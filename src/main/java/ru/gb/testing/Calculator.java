package ru.gb.testing;

public class Calculator {
    public long add(int x1, int x2) {
        return (long) x1 + x2;
    }

    public int sub(int x1, int x2) {
        return x1 - x2;
    }

    public int mul(int x1, int x2) {
        return x1 * x2;
    }

    public int div(int x1, int x2) {
        return x1 / x2;
    }
}
